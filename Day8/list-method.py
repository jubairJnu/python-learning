# Insert Method

a = [1,3,4]
a.insert(1,2)
print(a)

# Copy method

a = [2,4,2]
b = a.copy()
print(b)

# Count Method

a = [3,4,7,3]
print(a.count(3))

# Extend Method

a = [1,2]
a.extend([4,5,6])
print(a)