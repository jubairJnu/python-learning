#  Pop Method

a = [1,3,4,34]
print(a.pop())

# Remove

a = [1,45,345,23]
a.remove(45)
print(a)

# Reverse

a = [1,2,3,4]
a.reverse()
print(a)


# sort

a = [23,1,34,0, 20]
a.sort(reverse=True)
print(a)


# max Method

a = [23,9880,30]
print(max(a))

# clear
a = [23,9880,30]
a.clear()
print(a,"clear")